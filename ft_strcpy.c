#include "fillit.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	i;

	i = 0;
	while (1 == 1)
	{
		dst[i] = src[i];
		if (src[i] == '\0')
			break ;
		else
			i++;
	}
	return (dst);
}

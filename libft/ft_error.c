#include "libft.h"

void	ft_error(int i, char *mes)
{
	if (i != 1)
		return ;
	if (mes != NULL && mes[0] != '\0')
		ft_putstr_fd(mes, 2);
	else
		ft_putstr_fd("error\n", 2);
	exit(42);
}
